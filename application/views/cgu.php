		<h2>La machine à données Kesako ?</h2>
		Des entreprises identifiées par leur raison sociale, des noms de villes, de régions, d’académie et de département écrits en toute lettre, sans normalisation, avec fautes de frappe et abréviation. Autant de configuration de données qui handicapent l’utilisation des données et notamment le croisement de données issues de différentes sources.
		<br />
		Pour mobiliser simultanément différentes sources, il est commun de réintroduire a posteriori des identifiants interopérables issus de référentiels comme par exemple SIRENE (Insee), la Base centrale des établissements (BCE sur le système éducatif) ou encore le Répertoire national des structures de recherche (RNSR sur les unités de recherche). Des travaux de matching locaux voire du recodage manuel sont ainsi mis en œuvre. Ils s’avèrent couteux et longs.
		<br />
		La machine à données a pour objectif de réduire coûts et délais de mise en qualité de jeux de données dépourvus initialement d’identifiants interopérables. La machine à données permet à un utilisateur identifié
		<ul>
			<li>de décrire son jeu de données initial, </li>
			<li>d’identifier le référentiel auquel la source devra être mise en relation, </li>
			<li>de décrire par l’exemple les règles d’association entre ces deux éléments.</li>
		</ul>
		La machine à données apprend progressivement de l’interaction avec l’utilisateur les règles d’association et effectue le recodage sur l’ensemble du fichier source. A l’issue du traitement, l’utilisateur peut analyser et corriger si besoin, les recodages effectués par « La machine à données ». Les données lui sont restituées, enrichies d’une sélection des informations issues du référentiel qu’il a sélectionné.

		<h3>Un projet du programme « Entrepreneurs d’intérêt général »</h3>
		« La machine à données » est un projet conduit au sein du département des outils d’aide à la décision (Ministère de l’Enseignement supérieur, de la Recherche et de l’Innovation) dans le cadre du programme « Entrepreneur d’intérêt général » (1ere promotion) du programme des investissements d’avenir.
		<br />
		Ce programme permet d’intégrer pour une période de 10 mois des experts du numérique aux équipes des administrations pour mener à bien un projet précis. Cette configuration originale permet de produire rapidement des solutions opérationnelles et de développer l’ouverture des administrations à de nouveaux outils, de nouvelles méthodes.
